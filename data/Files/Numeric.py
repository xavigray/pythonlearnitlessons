# Introduction to Numeric Types in Python
# Integers
x = 42
y = -100
# Floating-point Numbers
a = 3.14
b = 2.5e-3
# Other Numeric Literals
hex_num = 0xFF # Hexadecimal
oct_num = 0o123 # Octal
bin_num = 0b1010 # Binary
complex_num = 2 + 3j # Complex
# Built-in Numeric Tools
# Expression Operators
# +, -, /, etc
# Built-in Mathematical Functions
# pow(), abs(), round(), int(), hex(), bin(), etc
# Utility Modules
# random, math, etc
result = 5 + 3 * 2 # Expression operators
absolute_value = abs(-10) #Built-in function

# Expression Operators
x = 10
y = 5
result = x + y * 2 # Addition and multiplication
print(result)
# Operator Precedence
a = 2 + 3 * 4 # Multiplication happens before addition
b = (2 + 3 ) * 4 # Parentheses override precedence
# Parentheses Grouping
x = (10 + 2) * 2 # Addition happens before multiplication
y = 10 + (5 * 2) # Multiplication happens before addition
# Mixed Type Conversion
result = 10 + 3.14 # Integer is converted to float
print(result)

